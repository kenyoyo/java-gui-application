    public void insertData(){
    if(checkDataInForm()){
       String PROD_NAME = productNameTxt.getText();
       double PROD_SALE_PRICE = 0.00;
       String PROD_CAPITAL_COST = productSellPriceTxt.getText();
       String PROD_DESCRIPT = DecriptionTxt.getText();
       int PROD_AMOUNT = 0;
       String PROD_MIN = productMinTxt.getText();
       int PROD_MANU = 0;
       
      String sql = "insert into product(PROD_NAME, PROD_SALE_PRICE, PROD_CAPITAL_COST, PROD_DESCRIPT, PROD_AMOUNT, PROD_MIN, PROD_MANU)\n" +
            "values('"+PROD_NAME+"', "+PROD_SALE_PRICE+", "+PROD_CAPITAL_COST+","+ "'"+PROD_DESCRIPT+"',"+PROD_AMOUNT+", "+PROD_MIN+", "+PROD_MANU+")";
     
        try{
           Connection conn = Gui_db.getConnection();
             Statement stmt  = conn.createStatement();
             stmt.executeUpdate(sql);
             clearTextBox();
             clearRow();
             ShowDataAgain();
             JOptionPane.showMessageDialog(this, "เพิ่มข้อมูลเรียบร้อย");
        
       }
       catch(SQLException ex) {
            System.out.println(ex.getMessage());
          }
       }  
        System.out.println(productNameTxt.getText() +" "+ DecriptionTxt.getText());
    }
    
    
    public void editData(){
       if(checkDataInForm()){
       String PROD_NAME = pdf_update.productNameTxt.getText();
       String PROD_DESCRIPT = pdf_update.DecriptionTxt.getText();
       String PROD_SALE_PRICE = pdf_update.productSellPriceTxt.getText();
       String PROD_MIN = productMinTxt.getText();
     
       
       String sql = "update product "+
               "set PROD_NAME = '"+ PROD_NAME + 
               "', PROD_DESCRIPT = '" +PROD_DESCRIPT+ 
               "', PROD_SALE_PRICE = "+ PROD_SALE_PRICE +
               ",PROD_MIN = " + PROD_MIN +
               " WHERE PROD_NAME = '" + pdf_update.setname+ "'";
        System.out.print(sql);       
        try{
            Connection conn = Gui_db.getConnection();
            Statement stmt = conn.createStatement();
            stmt.executeUpdate(sql);            
        } catch(SQLException ex){
            System.out.println(ex.getMessage());
        }       
        
        Product_Table PT  = new Product_Table();    
        PT.callClearRow();
        PT.ShowData();
        JOptionPane.showMessageDialog(null, "แก้ไขข้อมูลเรียบร้อยแล้ว");
        pdf_update.dispose();
       }  
    }
    
