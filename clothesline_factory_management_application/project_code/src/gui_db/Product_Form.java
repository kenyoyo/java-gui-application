    public boolean checkDataInForm() {
        if (!isNumeric(productSellPriceTxt.getText()) && !isNumeric(productMinTxt.getText())) {
            String message = "กรุณากรอกข้อมูลราคาขายต่อชิ้นและจำนวนต่ำสุด";
            JOptionPane.showMessageDialog(null, message);
            return false;
        }
        else if(!isNumeric(productSellPriceTxt.getText())){
            String message = "กรุณากรอกข้อมูลราคาขายต่อชิ้น";
            JOptionPane.showMessageDialog(null, message);
            return false;
        }
        else if(!isNumeric(productMinTxt.getText())){
            String message = "กรุณากรอกข้อมูลต่ำสุด";
            JOptionPane.showMessageDialog(null, message);
            return false;
        }               
        return true;
    }
    
     public static boolean isNumeric(String str) {
        return str.matches("-?\\d+(\\.\\d+)?");  //match a number with optional '-' and decimal.
    }
     
    
    
    
    
